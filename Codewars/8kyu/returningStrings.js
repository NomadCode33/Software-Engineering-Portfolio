/*
Create a function that accepts a parameter representing a name and 
returns the message: "Hello, <name> how are you doing today?".

function greet(name){
  //your code here
}
*/

// Answer
// Using the string literal, I returned message while using the 
// ${} to put the name put in the parameter.
function greet(name){
  return `Hello, ${name} how are you doing today?`
}